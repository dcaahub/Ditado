    private void botaoOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoOkActionPerformed
        try {
            palavra = campoDeTexto.getText();

            palavra = texto.comparaPalavra(palavra);
            if (!palavra.equals("#FIM#")) {

                if (palavra.contains("#")) {
                    areaDeTexto.setText(textoLido + palavra);

                } else {
                    textoLido += palavra;
                    areaDeTexto.setText(textoLido);
                    textoLido += " ";
                    campoDeTexto.setText("");
                }
            }

        } catch (FileNotFoundException ex) {
            Logger.getLogger(Janela.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_botaoOkActionPerformed

    private void campoDeTextoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campoDeTextoKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                palavra = campoDeTexto.getText();

                palavra = texto.comparaPalavra(palavra);
                if (!palavra.equals("#FIM#")) {

                    if (palavra.contains("#")) {
                        areaDeTexto.setText(textoLido + palavra);

                    } else {
                        textoLido += palavra;
                        areaDeTexto.setText(textoLido);
                        textoLido += " ";
                        campoDeTexto.setText("");
                    }
                }

            } catch (FileNotFoundException ex) {
                Logger.getLogger(Janela.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_campoDeTextoKeyPressed

    private void avancarBotaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_avancarBotaoActionPerformed
        inicio += QNTD_FRAMES;
        fim += QNTD_FRAMES;

        try {
            audio.stop();
            audio.play(inicio, fim);
        } catch (FileNotFoundException | JavaLayerException ex) {
            Logger.getLogger(Janela.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_avancarBotaoActionPerformed
 
    
    private void retrocederBotaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_retrocederBotaoActionPerformed
        inicio -= QNTD_FRAMES;
        fim -= QNTD_FRAMES;
        if (inicio < 0) {
            inicio = 0;
            fim = QNTD_FRAMES;
        }
        try {
            audio.stop();
            audio.play(inicio, fim);
        } catch (FileNotFoundException | JavaLayerException ex) {
            Logger.getLogger(Janela.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_retrocederBotaoActionPerformed

    private void proximoBotaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proximoBotaoActionPerformed
        indice += 1;
        audio.setNomeDoArquivo("audio" + indice + ".mp3");
        texto.setNomeDoArquivo("texto" + indice + ".txt");
        areaDeTexto.setText("");
        inicio = 0;
        fim = QNTD_FRAMES;
        try {
            tituloDoTexto.setText("Ditado: " + texto.pegaTitulo());
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Janela.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_proximoBotaoActionPerformed

